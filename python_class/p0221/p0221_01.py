# 주석
print('hello world1')
print("hello world2")

print(123)

print("안녕") # 한글도 입력가능
print('안녕'*10) # *를 사용해서 10번 반복

# , 로 두개(여러개)를 연결할 수 있다
print('안녕하세요. 반갑습니다')
print('안녕하세요.','반갑습니다')
print(30, '살 입니다')
print('안녕','hi','hello')
# ,를 사용하면 한칸 띄워진다.

# +를 사용해서 연결할 수 있다. (숫자는 불가능)
# +는 띄어쓰기가 없이 출력된다.
print('안녕하세요'+'반갑습니다')
print('30'+'살입니다')
print(str(30)+'살입니다')

